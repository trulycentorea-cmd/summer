/**
 * 주제를 하나 무작위로 고른다.
 * @param {object|null} avoid 방금 나온 주제. 연달아 같은 게 나오지 않게 피한다.
 */
export function pickTopic(avoid = null) {
  const pool = avoid ? TOPICS.filter(t => t.title !== avoid.title) : TOPICS;
  return pool[Math.floor(Math.random() * pool.length)];
}
